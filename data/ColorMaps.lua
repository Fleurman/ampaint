function convertToMap(s,name)
  local map = 'ABCDEFGHIJKLMNOPQRSTUVWXYZabcdefghijklmnopqrstuv'
  local done = 'Maps.' .. name .. ' = {\n'
  local c = 1
  for i in s:gmatch('[^\n]+') do
    done = done .. map[c] .. ' = ' .. i .. ',\n'
    c = c+1
  end
  done = done .. '}'
  local f = io.open('NewPalette.txt','w+')
  f:write(done)
  f:close()
end
