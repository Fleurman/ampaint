do
  for m,v in pairs(Src.raws) do
    if type(v) == 'table' then
      Src[m] = {}
      for mm,vv in pairs(v) do
        if type(vv) == 'table' then
          Src[m][mm] = {}
          for mmm,vvv in pairs(vv) do
            Src[m][mm][mmm] = Src.decode64(vvv)
          end
        else
          Src[m][mm] = Src.decode64(vv)
        end
      end
    else
      Src[m] = Src.decode64(v)
    end
  end
end

function tob64()
  local to64 = {'form','square','squarefull'}
  local tmp = ''
  for i,v in pairs(to64) do
    tmp = tmp..v..' = \''..Src.encode64('Exports/'..v..'.png')..'\',\n\n'
  end
  local f = io.open('base64.txt','w+')
  f:write(tmp)
  f:close()
  tmp = nil
end
