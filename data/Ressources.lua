function Src.encode64(name)
  local img = am.load_image(name)
  local buff = am.encode_png(img)
  local base = am.base64_encode(buff)
  return base
end
function Src.decode64(str)
  local base = am.base64_decode(str)
  local buff = am.decode_png(base)
  local tex = am.texture2d(buff)
  local spr = { 
                texture = tex,
                s1=0,
                t1=0,
                s2=1,
                t2=1,
                x1=0,
                y1=0,
                x2=tex.width,
                y2=tex.height,
                width=tex.width,
                height=tex.height,
              }
  return spr
end

